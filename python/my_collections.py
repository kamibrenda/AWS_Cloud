#list
myFruitList = ["apple", "cherry", "banana"]
print(myFruitList)
print(type(myFruitList))

print(myFruitList[0])
print(myFruitList[1])
print(myFruitList[2])

myFruitList[2] = "orange"
print(myFruitList)

#tuple
myTupleFruitList = ["apple", "orange", "pawpaw"]
print(myTupleFruitList)
print(myTupleFruitList[0])
print(myTupleFruitList[1])
print(myTupleFruitList[2])

